     * @return \Illuminate\Http\Response
     */
    public function store(StoreRequest $request, Post $post)
    {
        $input = $request->all();
        $input['user_id'] = \Auth::user()->id;

        $post->create($input);

        return redirect()->route('blog.admin.post.index');
    }

